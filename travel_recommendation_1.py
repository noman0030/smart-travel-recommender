# Sirf numeric features lo — ML numbers samajhta hai!
features = [
    'Budget_per_day_USD',
    'Adventure_score',
    'Culture_score',
    'Beach_score',
    'Nightlife_score',
    'Safety_score'
]

# StandardScaler — sab features ko same scale pe laao
# Warna Budget(40-300) Adventure(1-5) se zyada dominate karega!
# Iske baad sab 0 ke aas paas hoga

scaler = StandardScaler()           # Scaler machine banao
X = df[features]                    # Sirf numeric columns lo
