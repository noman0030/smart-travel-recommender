# ┌─────────────────────────────────────────────┐
# │  CELL 4 — K-Means Clustering               │
# │  Destinations ko groups mein baanto        │
# └─────────────────────────────────────────────┘

# K=5 matlab 5 types of travelers ke liye 5 groups
kmeans = KMeans(
    n_clusters=5,    # 5 groups chahiye
    random_state=42, # Reproducible results
    n_init=10        # 10 baar try karo, best lo
)

# fit_predict = train karo + label do ek saath!
df['Cluster'] = kmeans.fit_predict(X_scaled)

# Cluster numbers ko meaningful names do
cluster_names = {
    0: 'Budget Explorer',    # Kam budget, adventurous
    1: 'Luxury Traveler',    # High budget, comfort
    2: 'Beach & Party',      # Beach + nightlife lover
    3: 'Culture Seeker',     # History, art, food
    4: 'Adventure Junkie'    # Extreme adventures
}

df['Travel_Type'] = df['Cluster'].map(cluster_names)

print("Clustering complete! Har destination ka type:")
print(df[['City', 'Country', 'Budget_per_day_USD', 'Travel_Type']].to_string(index=False))


# ┌─────────────────────────────────────────────┐
# │  CELL 5 — Recommendation Function          │
# │  Tumhare liye best destinations dhundho    │
# └─────────────────────────────────────────────┘

def recommend_destinations(budget, adventure, culture,
                            beach, nightlife, safety, top_n=5):
    """
    Tumhara profile lo aur best destinations recommend karo!

    Parameters:
    -----------
    budget    : Roz kitna kharch kar sakte ho? (USD mein)
    adventure : Adventure kitna chahiye? (1=low, 5=high)
    culture   : Culture/history interest? (1=low, 5=high)
    beach     : Beach chahiye? (1=low, 5=high)
    nightlife : Nightlife chahiye? (1=low, 5=high)
    safety    : Safety priority? (1=low, 5=high)
    top_n     : Kitni recommendations chahiye?
    """

    # Step 1: Tumhara profile ek row mein
    user_profile = np.array([[budget, adventure, culture,
                               beach, nightlife, safety]])

    # Step 2: Same scaler se scale karo
    # (same scaler use karna ZAROORI hai!)
    user_scaled = scaler.transform(user_profile)

    # Step 3: Cosine Similarity
    # Tumhara profile vs har destination — kitna similar?
    # 1.0 = perfect match, 0.0 = bilkul alag
    similarities = cosine_similarity(user_scaled, X_scaled)[0]

    # Step 4: Similarity score add karo DataFrame mein
    df['Match_Score_%'] = (similarities * 100).round(1)

    # Step 5: Top N destinations sort karke do
    recommendations = df.nlargest(top_n, 'Match_Score_%')[
        ['City', 'Country', 'Budget_per_day_USD',
         'Travel_Type', 'Match_Score_%', 'Best_season']
    ].reset_index(drop=True)

    recommendations.index += 1  # 1 se start karo 0 se nahi
    return recommendations


